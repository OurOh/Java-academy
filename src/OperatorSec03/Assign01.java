package OperatorSec03;

public class Assign01 {

    public static void main(String[] args) {
        // 대입 연산자
        int a = 5;
        a += 3; //8
        System.out.println(a);
        a -= 2; //6
        System.out.println(a);
        a *= 4; //24
        System.out.println(a);
        a /= 3; //8
        System.out.println(a);
        a %= 5; // 3
        System.out.println(a);
    }
}
