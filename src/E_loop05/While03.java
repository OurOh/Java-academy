package E_loop05;

public class While03 {

    public static void main(String[] args) {

        int sum = 0;
        int i = 1;

        sum = sum +i;
        System.out.println("i = " + i + " sum = " + sum );
        i++;

        sum = sum +i;
        System.out.println("i = " + i + " sum = " + sum );
        i++;

        sum = sum +i;
        System.out.println("i = " + i + " sum = " + sum );
    }
}
